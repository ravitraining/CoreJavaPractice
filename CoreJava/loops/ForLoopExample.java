package loops;

public class ForLoopExample {
public static void main(String[] args) {
	// this is for the ++ incrament
	for(int i=0;i<10;i++)
	{
		System.out.print(i);
	}
	System.out.println();
	// this is for the decreament value
	for(int i=9;i>=0;i--)
	{
		System.out.print(i);
	}
}

}
