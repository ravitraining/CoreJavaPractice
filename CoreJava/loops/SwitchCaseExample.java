package loops;

public class SwitchCaseExample {
public static void main(String[] args) {
	int i=5;
	switch (i) {
	case 1:
		System.out.println("i am value 1");
		break;
	case 2:
		System.out.println("i am 2");
		break;
	default:
		System.out.println("I am none of the above");
		break;
	}
}
}
