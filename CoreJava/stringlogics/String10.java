package stringlogics;

import java.util.*;
// for count the string numbers
public class String10 {
	public static void main(String args[]) {
		Scanner sc = new Scanner(System.in);
		String s = sc.nextLine();
		int count = 0;
		do {
			while (true) {
				s.charAt(count);
				count++;

			}

		} while (true);
	}

}