package pattern2;

public class Pattern10
{
	public static void main(String args[])
	{
	   int i,j,num=4,n=4;
	                     
		for(i=num;i>=1;i--)
		{
		            
			for(j=1;j<=i;j++)
			{ 
				
			   System.out.print(n);
			  n--;
			}
			 System.out.println("");
			n=4;
			
		}	
		
	}



}