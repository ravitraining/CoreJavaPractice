package pattern2;
public class Pattern4
{
	public static void main(String args[])
	{
	   int i,j,num=4,n=0;
	
		for(i=1;i<=num;i++)
		{
		       n=4;
			for(j=1;j<=num;j++)
			{ 
				
			   System.out.print(n);
			    n--;
			}
			 System.out.println("");
			
			
		}	
		
	}



}